    $(document).ready(function () {
        var $scrollArea = $('ul.editorsRight.editors.EditorGroupUL');
        var $items = $scrollArea.children('li[data-start]');
        var currentIndex = $items.index($scrollArea.find('li.active'));
        var isScrolling = false;
        $scrollArea.on('wheel', function (e) {
            if (isScrolling) return;
            isScrolling = true;
            e.preventDefault();
            if (e.originalEvent.deltaY > 0) {
                if (currentIndex < $items.length - 1) {
                    currentIndex++;
                } else {
                    currentIndex = 0;
                }
            } else {
                if (currentIndex > 0) {
                    currentIndex--;
                } else {
                    currentIndex = $items.length - 1;
                }
            }
            var $targetItem = $items.eq(currentIndex);
            var scrollTo = $targetItem.position().top + $scrollArea.scrollTop();
            $scrollArea.stop().animate({ scrollTop: scrollTo }, 400, function () {
                isScrolling = false;
            });
            $items.removeClass('scrollActive');
            $targetItem.addClass('scrollActive');
        });
