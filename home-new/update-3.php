$(document).on('click','div.innerBody ul.heading li a',function(){
    $('div.innerBody ul.heading li a').removeClass('active');
    $('div.detailedBox').hide();
    $(this).addClass('active');
    if($(this).text()=='Edit'){
        var previewImage = $('div.editorBox ul li:first-child a').attr('data-image');
        $('body').attr('style','background: url('+previewImage+') center center;');
        $('div.editorBox').fadeIn();
    }
    if($(this).text()=='Color'){
        var previewImage = $('div.coloristBox ul li:first-child a').attr('data-image');
        $('body').attr('style','background: url('+previewImage+') center center;');
        $('div.coloristBox').fadeIn();
    }
    if($(this).text()=='Sound'){
        var previewImage = $('div.soundBox ul li:first-child a').attr('data-image');
        $('body').attr('style','background: url('+previewImage+') center center;');
        $('div.soundBox').fadeIn();
    }
    if($(this).text()=='VFX'){
        var previewImage = $('div.vfxBox ul li:first-child a').attr('data-image');
        $('body').attr('style','background: url('+previewImage+') center center;');
        $('div.vfxBox').fadeIn();
    }
});
$(document).on('mouseenter','div.innerBody div.detailedBox li a',function(){
    var previewImage = $(this).attr('data-image');
    $('body').attr('style','background: url('+previewImage+') center center;');
});
// loader start
document.addEventListener("DOMContentLoaded", function(event){
$(document).ready(function() {
    var video = document.getElementById('videoLoader');
    var isSafari = /^((?!chrome|android).)*safari/i.test(navigator.userAgent);
    if (isSafari) {
        $(video).on('click', function() {
            video.play();
        });
        video.muted = true;
    } else {
        video.play(); 
    }
    $(video).on('ended', function() {
        video.pause();
        $('div.videoMainSupport').hide();
        g_resize(); 
        pgal.init(); 
        setTimeout(function(){
            var checkContentLoaded = setInterval(function() {
                if ($('#gall').children().length>=120) {
                    if(firstAnimationAutovideo){
                        clearInterval(checkContentLoaded);
                        $('.particles-gallery-videos video:nth-child(1)').removeAttr('data-bs-target');
                        $('.particles-gallery-videos video:nth-child(1)').click();
                        $('.particles-gallery-videos video:nth-child(1)').attr('data-bs-target','#videoModalPopup');
                        firstAnimationAutovideo=false;
                    }
                }
            }, 100);
        }, 1500);
    });
});
});
// loader end
