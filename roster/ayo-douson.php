                                'padding-top': '0',
                                'padding-bottom': '2.5rem'
                            });
                        }
                    }
                
                if ($targetItem.length > 0) {
                    var scrollTo = $targetItem.position().top + $scrollArea.scrollTop() - ($scrollArea.height() - $targetItem.height()) / 2;
                    
